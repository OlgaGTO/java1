public class ForLoop {
    public static void main(String[] args) {
        char character = 'a';

        for (int i = 1; i <= 26; i++) {
            System.out.println("Litera curenta este: " + character + " si este litera cu numarul: " + i + " din alfabet. ");
            character++;
        }

        System.out.println("-----------");

        for (int i = 1; i<=101; i++) {
            System.out.println("Asta este dalmatianul cu numarul: " + i);
        }

    }
}




