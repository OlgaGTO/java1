public class Constante {
    public static void main(String[] args) {
        final int WEEKS=4; // O constanta reprezinta o adresa de memorie a carei valoare nu se poate schimba.
                           // O constanta poate sa fie definita folosind keywordul 'final'
                           // Prin conventie numele unei constante trebuie sa fie scris doar cu majuscule.
        final int HOURS_IN_A_DAY= 24;
        System.out.println(WEEKS);
        System.out.println(HOURS_IN_A_DAY);
        //WEEKS=5;

    }
}
